package com.android.cglib.proxy;

public class EnhancerProxyException extends RuntimeException {

  private static final long serialVersionUID = 702035040596969930L;

  public EnhancerProxyException() {
    super();
  }

  public EnhancerProxyException(String msg) {
    super(msg);
  }

  public EnhancerProxyException(String message, Throwable cause) {
    super(message, cause);
  }

  public EnhancerProxyException(Throwable cause) {
    super(cause);
  }
}
